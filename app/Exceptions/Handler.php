    );

    /**
     * Report or log an exception.
     *
     * This is a great spot to send exceptions to Sentry, Bugsnag, etc.
     *
     * @param  \Exception  $e
     * @return void
     */
    public function report(Exception $e)
    {
        return parent::report($e);
    }

    /**
     * Render an exception into an HTTP response.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \Exception  $e
     * @return \Illuminate\Http\Response
     */
    public function render($request, Exception $e)
    {
        if($e instanceOf ModelNotFoundException)
        {
        return response(view('errors.all', ['page_title' => '404', 'nav_active' => 'error', 'notice' => $this->http_codes[404]])->render(), 404);
        }

        if ($e instanceof HttpException || $e instanceof NotFoundHttpException) {
            $status = $e->getStatusCode();
            return response(view('errors.all', ['page_title' => $status, 'nav_active' => 'error', 'notice' => $this->http_codes[$status]])->render(), $status);
        }

        if (env('APP_DEBUG')) {
            return parent::render($request, $e);
        } else {
            return response(view('errors.all', ['page_title' => '500', 'nav_active' => 'error', 'notice' => $this->http_codes[500]]), 500);
        }
    }

}
